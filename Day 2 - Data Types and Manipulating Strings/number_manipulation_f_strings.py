print(8 / 3)
print(int(8 / 3))

## Rounding numbers using round()
print(round(8/3))

## Round to 2 decimal places
print(round(8/3 , 2))

## Floor division i.e drop the decimal places from the result using //
print(8 // 3)

## += -=
score = 5
score += 4
print(score)

score -= 7
print(score)

